class ReviewWordCount(MRJob):

	INPUT_PROTOCOL = JSONValueProtocol

	def review_mapper(self, _, data):
		if data['type'] != 'review':
			return
		funny = data['votes']['funny']
		cool = data['votes']['cool']
		useful = data['votes']['useful']

		norm = lambda word: re.sub('[^a-z]', '', word.lower())
		words = [norm(word) for word in data['text'].split()]
		for word in words:
			if word.lower() not in STOP_WORD:
				yield word.lower(), [1, funny, cool, useful]
			



	def review_reducer(self, word, votes):
		word_votes = list(votes)
		total = 0
		funny_present = 0
		cool_present = 0
		useful_present = 0
		not_funny = 0
		not_cool = 0
		not_useful = 0
		for vote in word_votes:
			if vote[1] > 0:
				funny_present += 1
			else:
				not_funny += 1
			if vote[2] > 0:
				cool_present += 1
			else:
				not_cool += 1
			if vote[3] > 0:
				useful_present += 1
			else:
				not_useful += 1
			

		if funny_present > MINIMUM_OCCURENCES or cool_present > MINIMUM_OCCURENCES or useful_present > MINIMUM_OCCURENCES: 
			yield word, [total, funny_present, not_funny, cool_present, not_cool, useful_present, not_useful ]

		


	def steps(self):
		return [
						self.mr(self.review_mapper, self.review_reducer)]

if __name__ == "__main__":
	ReviewWordCount().run()